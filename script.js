// I18N
// ============================================================
function setIdioma(lang) {
  const t = TRADUCOES[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const chave = el.dataset.i18n;
    if (t[chave] !== undefined) el.textContent = t[chave];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const chave = el.dataset.i18nHtml;
    if (t[chave] !== undefined) el.innerHTML = t[chave];
  });

  if (t['page-title']) document.title = t['page-title'];
  document.documentElement.lang = LANG_ATTR[lang] || lang;

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('idioma', lang);
  idiomaAtual = lang;
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => setIdioma(btn.dataset.lang));
});

const idiomasSalvo = localStorage.getItem('idioma');
setIdioma((idiomasSalvo && TRADUCOES[idiomasSalvo]) ? idiomasSalvo : 'pt');

