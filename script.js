// ANIMAÇÕES DE ENTRADA (IntersectionObserver)
// ============================================================
const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

if (!prefersReducedMotion) {
  // Seções e cards com data-animate
  const animObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        animObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.08 });

  document.querySelectorAll('[data-animate]').forEach(el => animObserver.observe(el));

  // Stagger nos itens de habilidades
  const staggerObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const items = entry.target.querySelectorAll('[data-animate-stagger]');
        items.forEach((item, i) => {
          item.style.transitionDelay = `${i * 60}ms`;
          item.classList.add('is-visible');
        });
        staggerObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15 });

  document.querySelectorAll('.hab-categoria').forEach(el => staggerObserver.observe(el));
}

