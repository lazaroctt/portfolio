// GITHUB STATS WIDGET
// ============================================================
async function carregarGithubStats() {
  const elRepos = document.getElementById('ghRepos');
  const elStars = document.getElementById('ghStars');
  const elLang  = document.getElementById('ghLang');

  try {
    const [userRes, reposRes] = await Promise.all([
      fetch('https://api.github.com/users/lazaroctt'),
      fetch('https://api.github.com/users/lazaroctt/repos?per_page=100&type=owner'),
    ]);

    if (!userRes.ok || !reposRes.ok) throw new Error('API error');

    const user  = await userRes.json();
    const repos = await reposRes.json();

    // Total de estrelas
    const totalStars = repos.reduce((acc, r) => acc + r.stargazers_count, 0);

    // Linguagem mais usada (por frequência de repos)
    const langCount = {};
    repos.forEach(r => {
      if (r.language) langCount[r.language] = (langCount[r.language] || 0) + 1;
    });
    const topLang = Object.entries(langCount).sort((a, b) => b[1] - a[1])[0];

    elRepos.textContent = user.public_repos ?? '—';
    elStars.textContent = totalStars > 0 ? totalStars : '—';
    elLang.textContent  = topLang ? topLang[0] : '—';

  } catch {
    // Fallback silencioso — mantém '—'
  }
}

carregarGithubStats();

